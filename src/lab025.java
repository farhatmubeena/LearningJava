public class lab025 {
    public static void main(String[] args) {
        //Ternary operator
        //it is rarely used
        //its like condition
        //? if true do this : else do this
        Boolean reult = 10>30 ? false : true;
        System.out.println(reult);


        int result = 10>40 ? 10 : 20;
        System.out.println(result);

        String s1 = 10>20 ? "ten" : "twenty";
        System.out.println(s1);
    }
}
